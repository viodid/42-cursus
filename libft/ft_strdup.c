#include "libft.h"

char	*ft_strdup(const char *str)
{
	int		len;
	char	*ptr;
	char	*start_ptr;

	len = ft_strlen(str);
	ptr = (char *)malloc(len + 1);
	if (!ptr)
		return (NULL);
	start_ptr = ptr;
	while (*str)
		*ptr++ = *(char *)str++;
	*ptr = '\0';
	return (start_ptr);
}
