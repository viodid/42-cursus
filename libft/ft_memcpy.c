#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	unsigned char	*ptr;

	ptr = (unsigned char *)dst;
	while (n--)
	{
		if (!dst && !src)
			return (dst);
		*ptr++ = *(unsigned char *)src++;
	}
	return (dst);
}
