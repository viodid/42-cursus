#include "get_next_line.h"

char	*ft_strchr(const char *s, int c)
{
	while (*s != (char)c)
		if (!*s++)
			return (NULL);
	return ((char *)s);
}

t_list	*create_node(char *content)
{
	t_list	*node;

	node = (t_list *) malloc(sizeof(t_list));
	if (!node)
		return (NULL);
	node->content = content;
	node->next = NULL;
	return (node);
}

int	append_node(t_list *header, t_list *node)
{
	if (!header || !node)
		return (0);
	while (header->next)
		header = header->next;
	header->next = node;
	return (1);
}